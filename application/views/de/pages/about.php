            <div id="welcome" style="border:0px"><h1>Über histat</h1><br/>
                <h3>Datenbank zur historischen Statistik</h3>
                <p class="normal">
                    Mit dem Datenbanksystem histat stellen wir hier ein Produkt zur Verfügung, das 2004 in der GESIS entwickelt wurde. Die Online-Plattform hat die Funktion, Zeitreihen aus der historischen, wirtschafts- und sozialwissenschaftlichen Forschung zugänglich zu machen. Der Zugriff auf die Datenbestände sollte auf der einen Seite möglichst einfach erfolgen, auf der anderen Seite wissenschaftlichen Ansprüchen genügen und die angebotenen Zeitreihen in ihrem Entstehungskontext hinreichend dokumentieren. 
                    Aus dieser Idee ist ein erfolgreiches Produkt geworden. Dieses Jahr werden wir über 3.000 registrierte Nutzerinnen und Nutzer aus der ganzen Welt verzeichnen. Die Datenbank umfasst mittlerweile über eine Viertelmillion Zeitreihen aus über 360 Studien. Im letzten Jahr wurden über 70.000 Zeitreihen heruntergeladen. Monatlich werden mehrere Studien ergänzt. 
                    Auch organisatorisch wurde histat auf eine solide Grundlage gesetzt. Mit der Auflösung des Zentrums für Historische Sozialforschung und der Etablierung einer Teamstruktur innerhalb von GESIS wird die Datenbank dauerhaft vom <a href="http://www.gesis.org/unser-angebot/daten-analysieren/daten-historische-studien/">Team Datenservice Historische Studien</a> betreut und weiterentwickelt. Innerhalb des zurzeit zehnköpfigen Teams widmen sich drei Mitarbeiterinnen und Mitarbeiter unterstützt von mehreren Hilfskräften der kontinuierlichen Bestandserweiterung und –pflege.  
                    2011 stellte die GESIS Mittel für einen umfassenden Relaunch bereit. Mit diesen Mitteln wurde die Oberfläche grundlegend überarbeitet und eine einheitliche Benutzerführung über alle Funktionen geschaffen. 
                </p>
                <br><br>
                <h3>Nutzungsstatistik</h3>
                <p class="normal">

                    Seit der Freischaltung verzeichnet histat eine jährlich wachsende Nutzung. Mit der Aufhebung der Registrierungspflicht zur Recherche im Jahre 2010 hat sich die Anzahl der Datenbankaufrufe innerhalb von zwei Jahren mehr als verdoppelt. Dieses Jahr werden wir über 3 000 registrierte Nutzerinnen und Nutzer verzeichnen.
