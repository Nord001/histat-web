<h1>HISTAT (Historische Statistik) - Zeitreihen Online</h1><br/>
<h3>Datenbank zur historischen Statistik</h3>
<p class="normal">
Auf diesen Seiten stellt Ihnen das <a href="http://www.gesis.org/unser-angebot/daten-analysieren/">GESIS Datenarchiv</a> für die Sozialwissenschaften Zeitreihen aus verschiedenen Projekten und Studien für die Recherche und den Download zur Verfügung.
</p>
<h3>Nutzung von HISTAT</h3>
<p class="normal">
Die Nutzung von HISTAT einschließlich des Downloads ist gebührenfrei.
Die weiteren Bedingungen für die Nutzung des Datenservices sind in der <a href="http://www.gesis.org/unser-angebot/daten-analysieren/datenservice/benutzungsordnung/">GESIS-Benutzungsordnung</a> festgelegt.
</p>
<h3>Zitierpflicht</h3>
<p class="normal">
Der Nutzer ist verpflichtet, die Quelle der verwendeten Daten und die zu den Daten angegebenen
Veröffentlichungen in den eigenen Publikationen entsprechend den wissenschaftlichen Gepflogenheiten zu zitieren.
Empfehlungen zur Zitierweise von Daten der Sozialforschung sind unter <a href="http://www.gesis.org/unser-angebot/daten-analysieren/datenservice/forschungsdaten-zitieren/">Datenzitat</a> und Beispiele für das Zitieren von Daten der historischen Sozialforschung unter <a href="http://www.gesis.org/unser-angebot/daten-analysieren/daten-historische-sozialf/datenzugang/">Datenzugang - Historische Sozialforschung</a> zu finden.
</p>